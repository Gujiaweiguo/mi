func newDocOutputHarness(t *testing.T, ctx context.Context, db *sql.DB) (*docoutput.Service, int64, int64, string) {
	t.Helper()
	workflowService := workflow.NewService(db, workflow.NewRepositoryWithNowFunc(db, docOutputWorkflowNow))
	leaseService := lease.NewService(db, lease.NewRepository(db), workflowService)
	billingRepo := billing.NewRepository(db)
	billingService := billing.NewService(db, billingRepo)
	invoiceService := invoice.NewService(db, invoice.NewRepository(db), billingRepo, workflowService)
	generatedPath := t.TempDir()
	service := docoutput.NewService(docoutput.NewRepository(db), invoiceService, config.StorageConfig{GeneratedDocumentsPath: generatedPath})
	activateLeaseForPrint(t, ctx, leaseService, workflowService, "CON-PRINT-101", "submit-print-lease-1")
	charges, err := billingService.GenerateCharges(ctx, billing.GenerateInput{PeriodStart: time.Date(2026, 4, 1, 0, 0, 0, 0, time.UTC), PeriodEnd: time.Date(2026, 4, 30, 0, 0, 0, 0, time.UTC), ActorUserID: 101})
	if err != nil {
		t.Fatalf("generate invoice charges: %v", err)
	}
	invoiceDoc, err := invoiceService.CreateFromCharges(ctx, invoice.CreateInput{DocumentType: invoice.DocumentTypeInvoice, BillingChargeLineIDs: []int64{charges.Lines[0].ID}, ActorUserID: 101})
	if err != nil {
		t.Fatalf("create invoice document: %v", err)
	}

	activateLeaseForPrint(t, ctx, leaseService, workflowService, "CON-PRINT-102", "submit-print-lease-2")
	charges, err = billingService.GenerateCharges(ctx, billing.GenerateInput{PeriodStart: time.Date(2026, 4, 1, 0, 0, 0, 0, time.UTC), PeriodEnd: time.Date(2026, 4, 30, 0, 0, 0, 0, time.UTC), ActorUserID: 101})
	if err != nil {
		t.Fatalf("generate bill charges: %v", err)
	}
	billDoc, err := invoiceService.CreateFromCharges(ctx, invoice.CreateInput{DocumentType: invoice.DocumentTypeBill, BillingChargeLineIDs: []int64{charges.Lines[0].ID}, ActorUserID: 101})
	if err != nil {
		t.Fatalf("create bill document: %v", err)
	}
	approvedInvoiceID := approveDocument(t, ctx, invoiceService, workflowService, invoiceDoc.ID, "invoice", 101)
	approvedBillID := approveDocument(t, ctx, invoiceService, workflowService, billDoc.ID, "bill", 101)
	return service, approvedInvoiceID, approvedBillID, generatedPath
}

func activateLeaseForPrint(t *testing.T, ctx context.Context, leaseService *lease.Service, workflowService *workflow.Service, leaseNo, submitKey string) {
	t.Helper()
	start := time.Date(2026, 4, 1, 0, 0, 0, 0, time.UTC)
	end := time.Date(2027, 3, 31, 0, 0, 0, 0, time.UTC)
	buildingID := int64(101)
	customerID := int64(101)
	brandID := int64(101)
	tradeID := int64(102)
	managementTypeID := int64(101)
	draft, err := leaseService.CreateDraft(ctx, lease.CreateDraftInput{LeaseNo: leaseNo, DepartmentID: 101, StoreID: 101, BuildingID: &buildingID, CustomerID: &customerID, BrandID: &brandID, TradeID: &tradeID, ManagementTypeID: &managementTypeID, TenantName: "ACME Retail", StartDate: start, EndDate: end, Units: []lease.UnitInput{{UnitID: 101, RentArea: 118}}, Terms: []lease.TermInput{{TermType: lease.TermTypeRent, BillingCycle: lease.BillingCycleMonthly, CurrencyTypeID: 101, Amount: 12000, EffectiveFrom: start, EffectiveTo: end}}, ActorUserID: 101})
	if err != nil {
		t.Fatalf("create lease draft: %v", err)
	}
	submittedLease, err := leaseService.SubmitForApproval(ctx, lease.SubmitInput{LeaseID: draft.ID, ActorUserID: 101, DepartmentID: 101, IdempotencyKey: submitKey, Comment: "submit lease"})
	if err != nil {
		t.Fatalf("submit lease: %v", err)
	}
	instance, err := workflowService.Approve(ctx, workflow.TransitionInput{InstanceID: *submittedLease.WorkflowInstanceID, ActorUserID: 101, DepartmentID: 101, IdempotencyKey: submitKey + "-step1", Comment: "manager approved"})
	if err != nil {
		t.Fatalf("approve lease step 1: %v", err)
	}
	if err := leaseService.SyncWorkflowState(ctx, instance, 101); err != nil {
		t.Fatalf("sync lease step 1: %v", err)
	}
	instance, err = workflowService.Approve(ctx, workflow.TransitionInput{InstanceID: *submittedLease.WorkflowInstanceID, ActorUserID: 101, DepartmentID: 101, IdempotencyKey: submitKey + "-step2", Comment: "finance approved"})
	if err != nil {
		t.Fatalf("approve lease step 2: %v", err)
	}
	if err := leaseService.SyncWorkflowState(ctx, instance, 101); err != nil {
		t.Fatalf("sync lease step 2: %v", err)
	}
}

func approveDocument(t *testing.T, ctx context.Context, invoiceService *invoice.Service, workflowService *workflow.Service, documentID int64, kind string, actorUserID int64) int64 {
	t.Helper()
	submitted, err := invoiceService.SubmitForApproval(ctx, invoice.SubmitInput{DocumentID: documentID, ActorUserID: actorUserID, DepartmentID: 101, IdempotencyKey: "submit-" + kind + "-print", Comment: "submit document"})
	if err != nil {
		t.Fatalf("submit %s document: %v", kind, err)
	}
	instance, err := workflowService.Approve(ctx, workflow.TransitionInput{InstanceID: *submitted.WorkflowInstanceID, ActorUserID: actorUserID, DepartmentID: 101, IdempotencyKey: "approve-" + kind + "-print", Comment: "finance approved"})
	if err != nil {
		t.Fatalf("approve %s document: %v", kind, err)
	}
	if err := invoiceService.SyncWorkflowState(ctx, instance, actorUserID); err != nil {
		t.Fatalf("sync %s document approval: %v", kind, err)
	}
	return documentID
}
