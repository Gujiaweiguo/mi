var docOutputWorkflowNow = func() time.Time { return time.Date(2026, 4, 1, 9, 0, 0, 0, time.UTC) }

func TestDocOutputServiceRendersInvoiceBatchGoldenHTMLAndPDF(t *testing.T) {
	ctx, cancel := context.WithTimeout(context.Background(), 5*time.Minute)
	defer cancel()
	db := platformdb.NewTestDB(t, ctx, os.DirFS("../platform/database"))
	service, invoiceID, _, generatedPath := newDocOutputHarness(t, ctx, db)

	if _, err := service.UpsertTemplate(ctx, docoutput.UpsertTemplateInput{Code: "invoice-batch-default", Name: "Invoice Batch Default", DocumentType: "invoice", OutputMode: docoutput.OutputModeInvoiceBatch, Title: "Lease Invoice Batch Print", Subtitle: "Default invoice batch layout", HeaderLines: []string{"Sunshine Commercial MI", "Finance Department"}, FooterLines: []string{"Approved invoice output", "Generated by MI"}, ActorUserID: 101}); err != nil {
		t.Fatalf("upsert invoice batch template: %v", err)
	}

	htmlArtifact, err := service.RenderHTML(ctx, docoutput.RenderInput{TemplateCode: "invoice-batch-default", DocumentIDs: []int64{invoiceID}, ActorUserID: 101})
	if err != nil {
		t.Fatalf("render invoice batch html: %v", err)
	}
	expectedHTML, err := os.ReadFile(filepath.Join("testdata", "invoice_batch_expected.html"))
	if err != nil {
		t.Fatalf("read expected invoice batch html: %v", err)
	}
	if strings.TrimSpace(string(htmlArtifact.Body)) != strings.TrimSpace(string(expectedHTML)) {
		t.Fatalf("invoice batch html mismatch\nexpected:\n%s\nactual:\n%s", string(expectedHTML), string(htmlArtifact.Body))
	}

	pdfArtifact, err := service.RenderPDF(ctx, docoutput.RenderInput{TemplateCode: "invoice-batch-default", DocumentIDs: []int64{invoiceID}, ActorUserID: 101})
	if err != nil {
		t.Fatalf("render invoice batch pdf: %v", err)
	}
	if pdfArtifact.ContentType != "application/pdf" || !strings.HasPrefix(string(pdfArtifact.Body), "%PDF") {
		t.Fatalf("expected pdf artifact, got contentType=%s size=%d", pdfArtifact.ContentType, len(pdfArtifact.Body))
	}
	if _, err := os.Stat(generatedPath); err != nil {
		t.Fatalf("expected generated documents path to exist: %v", err)
	}
}

